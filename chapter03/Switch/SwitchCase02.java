package chapter03.Switch;

import javax.swing.JOptionPane;

public class SwitchCase02 {

	public static void main(String[] args) {
		
		
		int num;
		num=Integer.parseInt(JOptionPane.showInputDialog("")); 
		
		switch (num) {
		case 1:
			System.out.println("");
			
			break;
		case 2:
			System.out.println("");
			
			break;
		case 3:
			System.out.println("");
			
			break;
		case 4:
			System.out.println("");
			
			break;

		default:
			System.out.println("");
			break;
		}//switch

	}//main

}//class
